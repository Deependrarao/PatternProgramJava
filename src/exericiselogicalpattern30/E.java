package exericiselogicalpattern30;

public class E {
	public static void main(String[] args) {
		for(int i=1; i<=20; i++) {
			if(i%5==0) {
				System.out.println(i +" Welcome" );
			}
			else if(i%2==0) {
				System.out.println(i +" hello");
			}
			else
			{
				System.out.println(i+ " hi");
			}
		}

	}

}
